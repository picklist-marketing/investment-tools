def load_config():
    with open(CONFIG_FILE, 'r') as f:
        return json.load(f)


def load_state():
    if os.path.exists(STATE_FILE):
        with open(STATE_FILE, 'r') as f:
            return json.load(f)
    return {"next_index": 0, "sent_log": []}


def save_state(state):
    with open(STATE_FILE, 'w') as f:
        json.dump(state, f, ensure_ascii=False, indent=2)


def send_line_message(config, message):
    token = os.environ.get("LINE_CHANNEL_ACCESS_TOKEN") or config["line"]["channel_access_token"]
    user_id = os.environ.get("LINE_USER_ID") or config["line"]["user_id"]
    if not token or not user_id:
        return False
    url = "https://api.line.me/v2/bot/message/push"
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {token}",
    }
    payload = {
        "to": user_id,
        "messages": [{"type": "text", "text": message[:5000]}],
    }
    try:
        resp = requests.post(url, headers=headers, json=payload, timeout=10)
        return resp.status_code == 200
    except:
        return False


def build_lesson_message(lesson, index):
    total = len(LESSONS)
    progress = f"[{index + 1}/{total}]"
    lines = [
        f"📚 投資の学校 {progress}",
        f"{lesson['phase']}",
        "",
        f"━━ {lesson['title']} ━━",
        "",
        lesson["body"],
    ]
    return "\n".join(lines)


def send_lesson():
    config = load_config()
    state = load_state()
    index = state["next_index"]

    if index >= len(LESSONS):
        # 全レッスン完了 → 最初に戻る
        index = 0
        print("  全レッスン完了！最初から繰り返します。")

    lesson = LESSONS[index]
    msg = build_lesson_message(lesson, index)

    print(msg)
    print("\n" + "=" * 50)

    if config["notify"]["line_enabled"]:
        success = send_line_message(config, msg)
        print("✅ LINE送信完了" if success else "❌ LINE送信失敗")
    else:
        print("⚠️ LINE通知が無効です")

    state["next_index"] = index + 1
    state["sent_log"].append({
        "index": index,
        "title": lesson["title"],
        "sent_at": datetime.now().isoformat(),
    })
    save_state(state)


def preview_all():
    for i, lesson in enumerate(LESSONS):
        print(f"\n{'=' * 50}")
        print(build_lesson_message(lesson, i))
    print(f"\n{'=' * 50}")
    print(f"\n全{len(LESSONS)}レッスン")


